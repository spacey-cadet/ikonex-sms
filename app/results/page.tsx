"use client";
import { useEffect, useState, Suspense } from "react";
import { useSearchParams } from "next/navigation";
import PageHeader from "@/components/PageHeader";
import Btn from "@/components/Btn";
import FormField, { inputStyle } from "@/components/FormField";
import { FileText, Download, TrendingUp } from "lucide-react";
import toast from "react-hot-toast";

const TERMS = ["Term 1","Term 2","Term 3"];
const YEARS = [2023,2024,2025,2026];

function gradeColor(grade: string): string {
  if (grade.startsWith("A")) return "#16a34a";
  if (grade.startsWith("B")) return "#1d4ed8";
  if (grade.startsWith("C")) return "#92400e";
  if (grade.startsWith("D")) return "#dc2626";
  return "#6b7280";
}

function ResultsInner() {
  const sp = useSearchParams();
  const [streams, setStreams] = useState<any[]>([]);
  const [results, setResults] = useState<any>(null);
  const [selStream, setSelStream] = useState(sp.get("streamId") || "");
  const [term, setTerm] = useState("Term 1");
  const [year, setYear] = useState(2025);
  const [loading, setLoading] = useState(false);
  const [view, setView] = useState<"class" | "individual">("class");
  const [selStudent, setSelStudent] = useState<any>(null);

  useEffect(() => { fetch("/api/streams").then(r => r.json()).then(d => setStreams(Array.isArray(d) ? d : [])); }, []);
  useEffect(() => { if (selStream) fetchResults(); }, [selStream, term, year]);

  const fetchResults = async () => {
    if (!selStream) return;
    setLoading(true);
    const r = await fetch(`/api/results?streamId=${selStream}&term=${encodeURIComponent(term)}&year=${year}`);
    const data = await r.json();
    setLoading(false);
    if (r.ok) { setResults(data); setSelStudent(null); }
    else toast.error(data.error || "Error loading results");
  };

  const downloadPDF = async (studentData?: any) => {
    const { default: jsPDF } = await import("jspdf");
    const { default: autoTable } = await import("jspdf-autotable");
    const doc = new jsPDF();
    const streamName = streams.find(s => s.id === selStream)?.name || "";

    if (studentData) {
      // Individual report card
      doc.setFont("helvetica", "bold");
      doc.setFontSize(18);
      doc.text("IKONEX ACADEMY", 105, 20, { align: "center" });
      doc.setFontSize(13);
      doc.setFont("helvetica", "normal");
      doc.text("STUDENT REPORT CARD", 105, 28, { align: "center" });
      doc.setFontSize(10);
      doc.text(`Term: ${term}  |  Year: ${year}  |  Stream: ${streamName}`, 105, 36, { align: "center" });

      doc.setDrawColor(15, 27, 45);
      doc.line(14, 40, 196, 40);

      doc.setFontSize(11);
      doc.setFont("helvetica", "bold");
      doc.text(`Name: ${studentData.firstName} ${studentData.lastName}`, 14, 48);
      doc.text(`Adm No: ${studentData.admNo}`, 14, 55);
      doc.text(`Class Position: ${studentData.position} of ${results.students.length}`, 120, 48);
      doc.text(`Mean Grade: ${studentData.meanGrade}`, 120, 55);

      autoTable(doc, {
        startY: 62,
        head: [["Subject", "Exam (80)", "CAT (20)", "Total (100)", "Grade", "Position"]],
        body: studentData.subjectResults.map((sr: any) => [
          sr.subjectName, sr.examScore, sr.catScore, sr.total, sr.grade, sr.position || "—"
        ]),
        foot: [["TOTAL", "", "", studentData.totalMarks, studentData.meanGrade, `${studentData.position}/${results.students.length}`]],
        styles: { fontSize: 10, cellPadding: 4 },
        headStyles: { fillColor: [15, 27, 45], textColor: 255, fontStyle: "bold" },
        footStyles: { fillColor: [201, 168, 76], textColor: [15, 27, 45], fontStyle: "bold" },
      });

      const finalY = (doc as any).lastAutoTable.finalY + 10;
      doc.setFontSize(9);
      doc.setFont("helvetica", "normal");
      doc.setTextColor(100);
      doc.text("Class Teacher: ___________________", 14, finalY + 14);
      doc.text("Principal: ___________________", 120, finalY + 14);
      doc.text("Generated by Ikonex Academy SMS", 105, 285, { align: "center" });

      doc.save(`ReportCard_${studentData.admNo}_${term}_${year}.pdf`);
    } else {
      // Class report
      doc.setFont("helvetica", "bold");
      doc.setFontSize(18);
      doc.text("IKONEX ACADEMY", 105, 20, { align: "center" });
      doc.setFontSize(13);
      doc.text(`CLASS PERFORMANCE REPORT — ${streamName}`, 105, 28, { align: "center" });
      doc.setFontSize(10);
      doc.setFont("helvetica", "normal");
      doc.text(`Term: ${term}  |  Year: ${year}`, 105, 35, { align: "center" });

      const subjectHeaders = results.subjects.map((s: any) => s.code);
      autoTable(doc, {
        startY: 42,
        head: [["Pos", "Adm No", "Student Name", ...subjectHeaders, "Total", "Avg", "Grade"]],
        body: results.students.map((st: any) => [
          st.position, st.admNo, `${st.firstName} ${st.lastName}`,
          ...results.subjects.map((s: any) => {
            const sr = st.subjectResults.find((r: any) => r.subjectId === s.id);
            return sr?.total || 0;
          }),
          st.totalMarks, st.average, st.meanGrade,
        ]),
        styles: { fontSize: 8, cellPadding: 2 },
        headStyles: { fillColor: [15, 27, 45], textColor: 255, fontStyle: "bold" },
        alternateRowStyles: { fillColor: [250, 248, 244] },
      });
      doc.save(`ClassReport_${streamName}_${term}_${year}.pdf`);
    }
    toast.success("PDF downloaded!");
  };

  return (
    <div>
      <PageHeader title="Results & Rankings" subtitle="View class performance and generate report cards" />

      <div style={{ background: "var(--white)", borderRadius: 12, padding: 20, border: "1px solid var(--border)", marginBottom: 20, display: "grid", gridTemplateColumns: "repeat(auto-fit, minmax(160px, 1fr))", gap: 12 }}>
        <FormField label="Class Stream">
          <select style={inputStyle} value={selStream} onChange={e => setSelStream(e.target.value)}>
            <option value="">-- Select Stream --</option>
            {streams.map((s: any) => <option key={s.id} value={s.id}>{s.name}</option>)}
          </select>
        </FormField>
        <FormField label="Term">
          <select style={inputStyle} value={term} onChange={e => setTerm(e.target.value)}>
            {TERMS.map(t => <option key={t}>{t}</option>)}
          </select>
        </FormField>
        <FormField label="Year">
          <select style={inputStyle} value={year} onChange={e => setYear(Number(e.target.value))}>
            {YEARS.map(y => <option key={y}>{y}</option>)}
          </select>
        </FormField>
        <div style={{ display: "flex", alignItems: "flex-end" }}>
          <Btn onClick={fetchResults} disabled={!selStream || loading} style={{ width: "100%", justifyContent: "center" }}>
            <TrendingUp size={14} />{loading ? "Loading..." : "Load Results"}
          </Btn>
        </div>
      </div>

      {!results ? (
        <div style={{ background: "var(--white)", borderRadius: 12, padding: "60px 24px", textAlign: "center", border: "1px solid var(--border)" }}>
          <FileText size={36} color="var(--slate-light)" style={{ marginBottom: 10 }} />
          <p style={{ color: "var(--slate)" }}>Select a stream and click "Load Results" to view performance</p>
        </div>
      ) : (
        <>
          {/* View toggle + download */}
          <div style={{ display: "flex", justifyContent: "space-between", alignItems: "center", marginBottom: 16 }}>
            <div style={{ display: "flex", gap: 4, background: "var(--white)", border: "1px solid var(--border)", borderRadius: 8, padding: 3 }}>
              {(["class", "individual"] as const).map(v => (
                <button key={v} onClick={() => setView(v)} style={{ padding: "6px 16px", borderRadius: 6, border: "none", cursor: "pointer", fontSize: 13, fontWeight: 600, background: view === v ? "var(--navy)" : "transparent", color: view === v ? "var(--white)" : "var(--slate)" }}>
                  {v === "class" ? "Class View" : "Individual"}
                </button>
              ))}
            </div>
            <div style={{ display: "flex", gap: 8 }}>
              <Btn variant="ghost" onClick={() => downloadPDF()}><Download size={13} /> Class PDF</Btn>
              {selStudent && <Btn onClick={() => downloadPDF(selStudent)}><Download size={13} /> Report Card</Btn>}
            </div>
          </div>

          {view === "class" ? (
            <div style={{ background: "var(--white)", borderRadius: 12, border: "1px solid var(--border)", overflow: "auto" }}>
              <table style={{ width: "100%", borderCollapse: "collapse", minWidth: 700 }}>
                <thead>
                  <tr style={{ background: "var(--navy)" }}>
                    {["Pos","Adm No","Student Name",...results.subjects.map((s: any) => s.code),"Total","Avg","Grade"].map((h: string) => (
                      <th key={h} style={{ padding: "10px 10px", textAlign: "center", fontSize: 11, fontWeight: 600, color: "var(--gold-light)", textTransform: "uppercase", letterSpacing: "0.06em", whiteSpace: "nowrap" }}>{h}</th>
                    ))}
                  </tr>
                </thead>
                <tbody>
                  {results.students.map((st: any, i: number) => (
                    <tr key={st.studentId} onClick={() => { setSelStudent(st); setView("individual"); }}
                      style={{ borderBottom: "1px solid var(--border)", cursor: "pointer", background: i % 2 === 0 ? "var(--white)" : "var(--cream)" }}>
                      <td style={{ padding: "8px 10px", textAlign: "center", fontWeight: 700, color: st.position <= 3 ? "var(--gold)" : "var(--navy)", fontSize: 14 }}>{st.position}</td>
                      <td style={{ padding: "8px 10px", textAlign: "center", fontSize: 11, fontFamily: "'DM Mono',monospace", color: "var(--slate)" }}>{st.admNo}</td>
                      <td style={{ padding: "8px 10px", fontWeight: 600, fontSize: 13, whiteSpace: "nowrap", color: "var(--navy)" }}>{st.firstName} {st.lastName}</td>
                      {results.subjects.map((s: any) => {
                        const sr = st.subjectResults.find((r: any) => r.subjectId === s.id);
                        return <td key={s.id} style={{ padding: "8px 10px", textAlign: "center", fontSize: 13, fontFamily: "'DM Mono',monospace" }}>{sr?.total || "—"}</td>;
                      })}
                      <td style={{ padding: "8px 10px", textAlign: "center", fontWeight: 700, fontSize: 14, fontFamily: "'DM Mono',monospace" }}>{st.totalMarks}</td>
                      <td style={{ padding: "8px 10px", textAlign: "center", fontSize: 13 }}>{st.average}</td>
                      <td style={{ padding: "8px 10px", textAlign: "center" }}>
                        <span style={{ fontWeight: 700, color: gradeColor(st.meanGrade), fontFamily: "'DM Mono',monospace", fontSize: 14 }}>{st.meanGrade}</span>
                      </td>
                    </tr>
                  ))}
                </tbody>
              </table>
            </div>
          ) : (
            <div>
              <div style={{ marginBottom: 16 }}>
                <select style={{ ...inputStyle, maxWidth: 300 }} value={selStudent?.studentId || ""} onChange={e => setSelStudent(results.students.find((s: any) => s.studentId === e.target.value))}>
                  <option value="">-- Select Student --</option>
                  {results.students.map((s: any) => <option key={s.studentId} value={s.studentId}>{s.firstName} {s.lastName} ({s.admNo})</option>)}
                </select>
              </div>
              {selStudent && (
                <div style={{ background: "var(--white)", borderRadius: 12, border: "1px solid var(--border)", overflow: "hidden" }}>
                  {/* Header */}
                  <div style={{ background: "var(--navy)", padding: "20px 24px", display: "flex", justifyContent: "space-between", alignItems: "center" }}>
                    <div>
                      <div style={{ fontFamily: "'Playfair Display',serif", fontSize: 20, color: "var(--white)", fontWeight: 700 }}>{selStudent.firstName} {selStudent.lastName}</div>
                      <div style={{ fontSize: 12, color: "var(--slate-light)", marginTop: 4 }}>Adm: {selStudent.admNo} · {term} {year} · {streams.find(s => s.id === selStream)?.name}</div>
                    </div>
                    <div style={{ textAlign: "right" }}>
                      <div style={{ fontSize: 36, fontWeight: 700, fontFamily: "'Playfair Display',serif", color: "var(--gold)" }}>{selStudent.meanGrade}</div>
                      <div style={{ fontSize: 12, color: "var(--slate-light)" }}>Position {selStudent.position}/{results.students.length}</div>
                    </div>
                  </div>
                  {/* Score table */}
                  <table style={{ width: "100%", borderCollapse: "collapse" }}>
                    <thead>
                      <tr style={{ background: "var(--cream)" }}>
                        {["Subject","Exam (80)","CAT (20)","Total (100)","Grade","Points","Position"].map(h => (
                          <th key={h} style={{ padding: "10px 16px", textAlign: "left", fontSize: 11, fontWeight: 600, color: "var(--slate)", textTransform: "uppercase", letterSpacing: "0.06em", borderBottom: "1px solid var(--border)" }}>{h}</th>
                        ))}
                      </tr>
                    </thead>
                    <tbody>
                      {selStudent.subjectResults.map((sr: any) => (
                        <tr key={sr.subjectId} style={{ borderBottom: "1px solid var(--border)" }}>
                          <td style={{ padding: "10px 16px", fontWeight: 500, fontSize: 14 }}>{sr.subjectName}</td>
                          <td style={{ padding: "10px 16px", fontSize: 14, fontFamily: "'DM Mono',monospace" }}>{sr.examScore}</td>
                          <td style={{ padding: "10px 16px", fontSize: 14, fontFamily: "'DM Mono',monospace" }}>{sr.catScore}</td>
                          <td style={{ padding: "10px 16px" }}>
                            <span style={{ fontWeight: 700, fontFamily: "'DM Mono',monospace", fontSize: 15, color: sr.total >= 50 ? "var(--green)" : sr.total >= 40 ? "var(--gold)" : "var(--red)" }}>{sr.total}</span>
                          </td>
                          <td style={{ padding: "10px 16px" }}>
                            <span style={{ fontWeight: 700, color: gradeColor(sr.grade), fontSize: 14 }}>{sr.grade}</span>
                          </td>
                          <td style={{ padding: "10px 16px", fontSize: 13, fontFamily: "'DM Mono',monospace" }}>{sr.points}</td>
                          <td style={{ padding: "10px 16px", fontSize: 13, color: "var(--slate)" }}>{sr.position || "—"}</td>
                        </tr>
                      ))}
                    </tbody>
                    <tfoot>
                      <tr style={{ background: "var(--navy)" }}>
                        <td style={{ padding: "12px 16px", fontWeight: 700, color: "var(--gold-light)" }}>TOTALS</td>
                        <td colSpan={2} />
                        <td style={{ padding: "12px 16px", fontWeight: 700, color: "var(--white)", fontFamily: "'DM Mono',monospace", fontSize: 16 }}>{selStudent.totalMarks}</td>
                        <td style={{ padding: "12px 16px", fontWeight: 700, color: "var(--gold)" }}>{selStudent.meanGrade}</td>
                        <td style={{ padding: "12px 16px", color: "var(--slate-light)", fontSize: 13 }}>{selStudent.totalPoints} pts</td>
                        <td style={{ padding: "12px 16px", fontWeight: 700, color: "var(--gold-light)" }}>{selStudent.position}/{results.students.length}</td>
                      </tr>
                    </tfoot>
                  </table>
                  <div style={{ padding: "14px 20px", borderTop: "1px solid var(--border)", display: "flex", justifyContent: "flex-end" }}>
                    <Btn onClick={() => downloadPDF(selStudent)}><Download size={13} /> Download Report Card PDF</Btn>
                  </div>
                </div>
              )}
            </div>
          )}
        </>
      )}
    </div>
  );
}

export default function ResultsPage() {
  return <Suspense fallback={<div>Loading...</div>}><ResultsInner /></Suspense>;
}
